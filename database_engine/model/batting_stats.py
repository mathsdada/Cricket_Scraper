class BattingStats:
    def __init__(self, batsman_id, match_id, innings_num, runs_scored, balls_played, num_fours, num_sixes, playing_team):
        self.batsman_id = batsman_id
        self.match_id = match_id
        self.innings_num = innings_num
        self.runs_scored = runs_scored
        self.balls_played = balls_played
        self.num_fours = num_fours
        self.num_sixes = num_sixes
        self.playing_team = playing_team
