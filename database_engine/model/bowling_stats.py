class BowlingStats:
    def __init__(self, bowler_id, match_id, innings_num, wickets_taken, overs_bowled, runs_given, economy, playing_team):
        self.bowler_id = bowler_id
        self.match_id = match_id
        self.innings_num = innings_num
        self.wickets_taken = wickets_taken
        self.overs_bowled = overs_bowled
        self.runs_given = runs_given
        self.economy = economy
        self.playing_team = playing_team
